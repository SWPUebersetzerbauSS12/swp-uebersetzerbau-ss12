#include <stdio.h>

int main()
{
	int a = 10;
	int b = 3;
	int erg;
	
	erg = a + b;
	printf("%d + %d = %d\n", a, b, erg);
	
	erg = a - b;
	printf("%d - %d = %d\n", a, b, erg);
	
	erg = a * b;
	printf("%d * %d = %d\n", a, b, erg);
	
	erg = a / b;
	printf("%d / %d = %d\n", a, b, erg);
	
	return 0;
}

	
